public class VariabelTipeData {
    public static void main(String[] args) {
        // deklarasi variabel
        String kalimat;
        int angka;

        // inisialisasi variabel
        kalimat = "Hello World";
        angka = 10;

        // deklarasi & inisialisasi variabel
        double floatingPoint = 10.5;

        // print variabel
        System.out.println(kalimat);
        System.out.println(angka);
        System.out.println(floatingPoint);
    }
}
