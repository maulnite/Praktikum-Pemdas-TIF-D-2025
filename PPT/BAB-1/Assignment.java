public class Assignment {
    public static void main(String[] args) {
        int num = 5; // num = 5

        // operator +=
        num += 5; // num = num + 5 = 10
        System.out.println("Setelah += : " + num);

        // operator -=
        num -= 5; // num = num - 5 = 5
        System.out.println("Setelah -= : " + num);

        // operator *=
        num *= 5; // num = num * 5 = 25
        System.out.println("Setelah *= : " + num);

        // operator /=
        num /= 5; // num = num / 5 = 5
        System.out.println("Setelah /= : " + num);

        // operator %=
        num %= 5; // num = num % 5 = 0
        System.out.println("Setelah %= : " + num);
    }
}